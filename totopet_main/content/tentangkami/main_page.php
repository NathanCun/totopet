            <p>TotoPet Indonesia pertama kali didirikan pada tahun 2018 merupakan perusahaan yang bergerak dibidang pemasok makanan khusus hewan kesayangan (pet supplies) dan keperluan hewan lainnya.</p>
                <br>
            <p>Seiring perkembangan jaman, antusiasme masyarakat untuk memelihara hewan kesayangan semakin besar, berbagai riset telah membuktikan bahwa banyak manfaat yang bisa diperoleh dari hewan peliharaan.</p>
                <br>
            <p>Untuk menjawab keinginan pasar yang semakin tinggi TotoPet kini hadir untuk memudahkan anda para pecinta hewan di seluruh Indonesia untuk memenuhi berbagai keperluan hewan kesayangan anda seperti makanan kucing, makanan anjing, rumah untuk anabul serta berbagai keperluan sehari-hari seperti cat litter, snack, maupun aksesoris.</p>
        </div>

